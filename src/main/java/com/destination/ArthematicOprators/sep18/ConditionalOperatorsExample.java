package com.destination.ArthematicOprators.sep18;

public class ConditionalOperatorsExample {

	public static void main(String[] args) {
		System.out.println("=====Conditional Operators Example=====");
		int a=100;
		int b=50;
		System.out.println("value no:"+a);
		System.out.println("value no:"+b);
		System.out.println("============");
		boolean res1=a>b;
		System.out.println("Greater then result:"+res1);
		System.out.println("============");
		boolean res2=a<b;
		System.out.println("less then result:"+res2);
		System.out.println("=============");
		boolean res3=a>=b;
		System.out.println("greater then or equal to result:"+res3);
		System.out.println("===========");
		boolean res4=a<=b;
		System.out.println("less then or equal to result:"+res4);
		System.out.println("============");
		boolean res5=a==b;
		System.out.println("equals to result:"+res5);
		System.out.println("============");
		boolean res6=a!=b;
		System.out.println("not equals to result:"+res6);
		System.out.println("============"); 

	}

}
